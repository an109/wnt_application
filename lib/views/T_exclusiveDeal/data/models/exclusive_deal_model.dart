import '../../domain/entities/exclusive_deal_entity.dart';

class ExclusiveDealModel extends ExclusiveDealEntity {
  const ExclusiveDealModel({
    required int id,
    required int reseller,
    required String ownerTab,
    required String category,
    required String brand,
    required String title,
    required String imageUrl,
    required String description,
    required String shortDescription,
    required String discountText,
    required String couponCode,
    required String validUpto,
    required String sectorType,
    required dynamic linkUrl,
    required bool isHotDeal,
    required String country,
    required bool isFixedDeparture,
    required bool isCoachTour,
    required int order,
    required bool isActive,
    required DateTime created,
    required DateTime updated,
  }) : super(
    id: id,
    reseller: reseller,
    ownerTab: ownerTab,
    category: category,
    brand: brand,
    title: title,
    imageUrl: imageUrl,
    description: description,
    shortDescription: shortDescription,
    discountText: discountText,
    couponCode: couponCode,
    validUpto: validUpto,
    sectorType: sectorType,
    linkUrl: linkUrl,
    isHotDeal: isHotDeal,
    country: country,
    isFixedDeparture: isFixedDeparture,
    isCoachTour: isCoachTour,
    order: order,
    isActive: isActive,
    created: created,
    updated: updated,
  );

  factory ExclusiveDealModel.fromJson(Map<String, dynamic> json) {
    return ExclusiveDealModel(
      id: json['id'] ?? 0,
      reseller: json['reseller'] ?? 0,
      ownerTab: json['owner_tab'] ?? '',
      category: json['category'] ?? '',
      brand: json['brand'] ?? '',
      title: json['title'] ?? '',
      imageUrl: json['image_url'] ?? '',
      description: json['description'] ?? '',
      shortDescription: json['short_description'] ?? '',
      discountText: json['discount_text'] ?? '',
      couponCode: json['coupon_code'] ?? '',
      validUpto: json['valid_upto'] ?? '',
      sectorType: json['sector_type'] ?? '',
      linkUrl: json['link_url'],
      isHotDeal: json['is_hot_deal'] ?? false,
      country: json['country'] ?? '',
      isFixedDeparture: json['is_fixed_departure'] ?? false,
      isCoachTour: json['is_coach_tour'] ?? false,
      order: json['order'] ?? 0,
      isActive: json['is_active'] ?? false,
      created: DateTime.parse(json['created']),
      updated: DateTime.parse(json['updated']),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'reseller': reseller,
      'owner_tab': ownerTab,
      'category': category,
      'brand': brand,
      'title': title,
      'image_url': imageUrl,
      'description': description,
      'short_description': shortDescription,
      'discount_text': discountText,
      'coupon_code': couponCode,
      'valid_upto': validUpto,
      'sector_type': sectorType,
      'link_url': linkUrl,
      'is_hot_deal': isHotDeal,
      'country': country,
      'is_fixed_departure': isFixedDeparture,
      'is_coach_tour': isCoachTour,
      'order': order,
      'is_active': isActive,
      'created': created.toIso8601String(),
      'updated': updated.toIso8601String(),
    };
  }
}